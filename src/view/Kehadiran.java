    private void showData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Kehadiran");
        model.addColumn("Nama Peserta");
        model.addColumn("Instansi");

        try {
            String sql = "SELECT id, kehadiran, nama, instansi FROM kehadiran";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("kehadiran"),
                    rs.getString("nama"),
                    rs.getString("instansi"),});
            }

            TPeserta.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data");
        }
    }

