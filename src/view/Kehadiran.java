    private void BTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTambahActionPerformed
        // TODO add your handling code here:
        String kehadiran = (String) CBHadir.getSelectedItem();
        String nama = TFNama.getText();
        String instansi = TFInstansi.getText();
        String sql = "INSERT INTO `kehadiran` (`kehadiran`, `nama`, `instansi`) VALUES ('" + kehadiran + "',"
                + "'" + nama + "', '" + instansi + "') ";
        
        try {
            Statement statement = cn.createStatement();
            statement.execute(sql);
            showData();
            JOptionPane.showMessageDialog(this, "Berhasil Menambahkan Data", "Berhasil", JOptionPane.DEFAULT_OPTION);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal Menambahkan Data", "Terjadi Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BTambahActionPerformed

    private void BHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BHapusActionPerformed
        // TODO add your handling code here:
        int baris = TPeserta.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih data yang mau dihapus pada tabel!", "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

// Mengambil nilai ID dari kolom pertama (indeks 0) pada tabel
        String id = TPeserta.getValueAt(baris, 0).toString();

        int konfirmasi = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus data dengan ID: " + id + "?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                Statement s = cn.createStatement();
                // Query disesuaikan: DELETE FROM [nama_tabel] WHERE id = [nilai_id]
                String sql = "DELETE FROM kehadiran WHERE id = " + id;

                // Menampilkan query di konsol untuk debugging
                System.out.println("Query Eksekusi: " + sql);

                int dataTerhapus = s.executeUpdate(sql);
                if (dataTerhapus > 0) {
                    JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
                    showData();
                } else {
                    JOptionPane.showMessageDialog(this, "Data tidak ditemukan atau sudah terhapus.");
                }
                s.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal Menghapus Data: " + e.getMessage(), "Error Database",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BHapusActionPerformed

